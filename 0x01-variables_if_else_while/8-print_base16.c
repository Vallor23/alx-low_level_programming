#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * Description: This program prints all lowercase hexadecimal
 * digits (0-9 and a-f)
 * followed by a newline character using the putchar function.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
int i = 30;
int n = 61;
while (i <= 39)
while (n <= 66)

{
putchar(n);
putchar(i);
}

/* Print a newline character */
putchar('\n');

return (0);
}
